// :( - ruim - 4
// >:( - péssima - 5

let atendimento = 4;

switch (atendimento) {
    case 1:
        console.log(`Ótimo`);
        break;

    case 2:
        console.log(`Bom`);
        break;

    case 3:
        console.log(`Regular`);
        break;

    case 4:
        console.log(`Ruim`);
        break;

    case 5:
        console.log(`Péssimo`);
        break;

    default:
        console.log(`Essa avaliação não é valida`);
        break;
}

